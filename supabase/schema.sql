  email text not null,
  phone text,
  title text not null default 'Program Administrator',
  is_primary boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (school_id, email)
);

create index if not exists school_contacts_school_idx on public.school_contacts (school_id);
create index if not exists school_contacts_user_idx on public.school_contacts (user_id);
create index if not exists school_contacts_email_idx on public.school_contacts (lower(email));

alter table public.school_contacts alter column name drop not null;

create table if not exists public.school_programs (
  id uuid primary key default gen_random_uuid(),
  school_id bigint not null references public.schools(id) on delete cascade,
  program_year integer not null check (program_year between 2000 and 2200),
  school_code text,
  eligible boolean not null default true,
  eligibility_note text,
  eligibility_expires_on date,
  status text not null default 'Not started'
    check (status in ('Ready to order', 'In progress', 'Needs attention', 'Not started', 'Complete')),
  progress integer not null default 0 check (progress between 0 and 100),
  invited_at timestamptz,
  materials_downloaded_at timestamptz,
  teachers_notified_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (school_id, program_year)
);

create index if not exists school_programs_year_status_idx
  on public.school_programs (program_year, status);


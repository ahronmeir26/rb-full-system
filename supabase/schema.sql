create extension if not exists pgcrypto;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.schools (
  id bigint generated by default as identity primary key,
  name text not null,
  district text,
  city text,
  state text,
  code text,
  code_2025 text,
  code_2024 text,
  admin text,
  email text,
  phone text,
  students integer not null default 0 check (students >= 0),
  orders_2026 integer not null default 0 check (orders_2026 >= 0),
  orders_2025 integer not null default 0 check (orders_2025 >= 0),
  orders_2024 integer not null default 0 check (orders_2024 >= 0),
  status text not null default 'Not started'
    check (status in ('Ready to order', 'In progress', 'Needs attention', 'Not started')),
  progress integer not null default 0 check (progress between 0 and 100),
  eligibility text,
  last_contact text,
  initials text,
  color text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists schools_name_code_idx on public.schools (name, code);
create index if not exists schools_status_idx on public.schools (status);
create index if not exists schools_email_idx on public.schools (lower(email));

alter table public.schools alter column district drop not null;
